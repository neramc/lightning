//! Writes `packages/bindings/src/index.ts` from the specta type registry and
//! the [`lightning_ipc_types::COMMANDS`] table. Invoked by `pnpm bindings`
//! (scripts/gen-bindings.mjs); CI checks freshness with `pnpm bindings
//! --check`.

use std::fmt::Write as _;

use lightning_ipc_types as ipc;
use specta::TypeCollection;
use specta_typescript::Typescript;

fn camel_case(snake: &str) -> String {
    let mut out = String::with_capacity(snake.len());
    let mut upper_next = false;
    for ch in snake.chars() {
        if ch == '_' {
            upper_next = true;
        } else if upper_next {
            out.extend(ch.to_uppercase());
            upper_next = false;
        } else {
            out.push(ch);
        }
    }
    out
}

fn main() {
    let out_path = std::env::args()
        .nth(1)
        .expect("usage: export-bindings <out-file>");

    let mut types = TypeCollection::default();
    types
        .register::<ipc::ContentDto>()
        .register::<ipc::ParamValueDto>()
        .register::<ipc::ErrorPolicyDto>()
        .register::<ipc::BranchDto>()
        .register::<ipc::StepDto>()
        .register::<ipc::IconDto>()
        .register::<ipc::TriggerConfigDto>()
        .register::<ipc::ShortcutDto>()
        .register::<ipc::SupportLevelDto>()
        .register::<ipc::PlatformSupportDto>()
        .register::<ipc::ParamDefDto>()
        .register::<ipc::ActionDefDto>()
        .register::<ipc::CapabilityStatusDto>()
        .register::<ipc::CapabilitySnapshotDto>()
        .register::<ipc::RunProgressDto>()
        .register::<ipc::RunLogEntryDto>()
        .register::<ipc::RunResultDto>()
        .register::<ipc::ShortcutMetaDto>()
        .register::<ipc::RunRecordDto>();

    let type_decls = Typescript::default()
        .export(&types)
        .expect("specta TypeScript export failed");

    let mut src = String::new();
    src.push_str(
        "// @generated by `pnpm bindings` (crates/ipc-types/src/bin/export_bindings.rs)\n\
         // DO NOT EDIT — regenerate after touching any Rust command/event/type (CLAUDE.md §2.4).\n\
         // SPDX-License-Identifier: GPL-3.0-only\n\
         // Copyright (C) 2026 neramc\n\n\
         import { invoke } from '@tauri-apps/api/core';\n\
         import { listen, type UnlistenFn } from '@tauri-apps/api/event';\n\n",
    );
    src.push_str(&type_decls);
    src.push('\n');

    // Command wrappers — components never call invoke() directly (§10).
    for command in ipc::COMMANDS {
        let fn_name = camel_case(command.name);
        let params = command
            .args
            .iter()
            .map(|(name, ty)| format!("{name}: {ty}"))
            .collect::<Vec<_>>()
            .join(", ");
        let payload = if command.args.is_empty() {
            String::new()
        } else {
            let fields = command
                .args
                .iter()
                .map(|(name, _)| *name)
                .collect::<Vec<_>>()
                .join(", ");
            format!(", {{ {fields} }}")
        };
        let _ = writeln!(
            src,
            "export async function {fn_name}({params}): Promise<{result}> {{\n  return await invoke<{result}>('{name}'{payload});\n}}\n",
            result = command.result,
            name = command.name,
        );
    }

    // Event names + typed subscription helpers (§6.8).
    src.push_str(&format!(
        "export const EVENTS = {{\n  runProgress: '{run}',\n  triggersFired: '{trig}',\n  capabilityChanged: '{cap}',\n  storeChanged: '{store}',\n}} as const;\n\n",
        run = ipc::EVENT_RUN_PROGRESS,
        trig = ipc::EVENT_TRIGGERS_FIRED,
        cap = ipc::EVENT_CAPABILITY_CHANGED,
        store = ipc::EVENT_STORE_CHANGED,
    ));
    src.push_str(
        "export function onRunProgress(\n  handler: (payload: RunProgressDto) => void,\n): Promise<UnlistenFn> {\n  return listen<RunProgressDto>(EVENTS.runProgress, (event) => handler(event.payload));\n}\n\n\
         export function onCapabilityChanged(\n  handler: (payload: CapabilitySnapshotDto) => void,\n): Promise<UnlistenFn> {\n  return listen<CapabilitySnapshotDto>(EVENTS.capabilityChanged, (event) =>\n    handler(event.payload),\n  );\n}\n\n\
         export function onStoreChanged(handler: (shortcutId: string) => void): Promise<UnlistenFn> {\n  return listen<string>(EVENTS.storeChanged, (event) => handler(event.payload));\n}\n",
    );

    std::fs::write(&out_path, src).expect("failed to write bindings file");
    eprintln!("export-bindings: wrote {out_path}");
}
